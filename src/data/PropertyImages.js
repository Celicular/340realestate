/**
 * Get images for a property by ID or title
 * @param {string} identifier - Property ID or title
 * @returns {Array} - Array of image imports, or empty array if not found
 */
export const getPropertyImages = (identifier) => {
  if (!identifier) return [];
  
  const key = identifier.toLowerCase().trim();
  
  // Direct match
  if (propertyImagesMap[key]) {
    return propertyImagesMap[key];
  }
  
  // Partial match - find if identifier contains any key
  for (const [mapKey, images] of Object.entries(propertyImagesMap)) {
    if (key.includes(mapKey) || mapKey.includes(key)) {
      return images;
    }
  }
  
  return [];
};

